class User < ApplicationRecord
  validates :email, presence: true, uniqueness:true, format: { with: /\A([^@\s]+)@((?:[-a-z0-9]+\.)+[a-z]{2,})\z/i, on: :create }
  validates :password, presence: true
  # has_secure_password
  has_many :todos, foreign_key: :user_id, inverse_of: :user

end
